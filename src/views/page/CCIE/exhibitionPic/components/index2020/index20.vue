           if(item.HALLNO == '8.1'){
            this.alleight = item
          }
          if(item.HALLNO == '8.2'){
            this.alleight1 = item
          }
        })
      })
    },
    goDetail(isClick, num) {
      //  return false
      if(num =='人文展区'){
        this.$Message.error('该展区暂无展位')
        return false
      }
      if(isClick){
        this.$router.push({name:'rangerInfor',params:{numSite:num}})
        setCookie('hallType',desicription[num].title)
        setCookie('hallNo',num)
      }
    },
    showDes(e, isClick, num) {
      if (!isClick) {
        return false;
      }
      var x = e.pageX,
        y = e.pageY;
      
      this.numsite=num;
      this.show = true;
      this.des.title = desicription[num]['title'];
      this.des.A = desicription[num]['A'];
      this.des.B = desicription[num]['B'];
      this.des.C = desicription[num]['C'];
      if( this.listArray.length == 0){
        this.llNum =0
        this.gfxNum =0
        this.zdgzNum =0

        this.zjZW = 0
        this.zjZS = 0
        this.zjCountry = 0
      }else{
        let filterArray =  this.listArray.filter((item)=>item.HALLNO == num)
        let filterRisk =  this.riskList.filter((item)=>item.HALLNO == num)
        this.llNum = filterRisk[0] && filterRisk[0].CNUM ? filterRisk[0].CNUM :0
        this.gfxNum = filterRisk[0] && filterRisk[0].DNUM ? filterRisk[0].DNUM :0
        this.zdgzNum = filterRisk[0] && filterRisk[0].BNUM ? filterRisk[0].BNUM :0

        this.zjZW = filterArray[0] && filterArray[0].POSITIONNUM ? filterArray[0].POSITIONNUM :0
        this.zjZS =filterArray[0] &&  filterArray[0].EXHIBITORNUM ? filterArray[0].EXHIBITORNUM :0
        this.zjCountry =filterArray[0] &&  filterArray[0].COUNTTRYNUM ? filterArray[0].COUNTTRYNUM :0
      }
    

    this.$refs.tooltip.style.left = x + 30 + "px";
    if(y<625)
    this.$refs.tooltip.style.top = y + 30 + "px";
    else
    this.$refs.tooltip.style.top = y - 100 + "px";
    },
    hideDes() {
      this.show = false;
      
    },
    loadTooltip() {}
  }
};
